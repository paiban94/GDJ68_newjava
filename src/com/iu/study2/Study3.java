package com.iu.study2;

public class Study3 {
	public static void main(String[] args) {
		//if, if-else, if-else if-else
		//switch() case
		int num=5;
		//case에 들어가는 수는 상수, 변수x
		switch(num*2) {
		case 10:
			System.out.println("num 10");
			break;
		case 20:
			System.out.println("num 20");
			break;
		default:
			System.out.println("num 모름");
	
		}
	
	}
}	
